function formatItem(item) {
    if (typeof item === "string") {
        return item;
    }

    if (typeof item === "object" && item !== null) {
        const name = item.name || item.api || item.endpoint || item.table || item.module || item.title || "Item";
        const description = item.description || item.purpose || item.details || item.function || "";

        if (description) {
            return `${name} - ${description}`;
        }

        return name;
    }

    return String(item);
}

function createBlueprintSection(title, content, type) {
    if (!content) {
        content = type === "paragraph" ? "Not provided." : [];
    }

    if (type === "paragraph") {
        return `
            <div class="blueprint-section">
                <h5>${title}</h5>
                <p>${content}</p>
            </div>
        `;
    }

    if (!Array.isArray(content)) {
        content = [content];
    }

    return `
        <div class="blueprint-section">
            <h5>${title}</h5>
            <ul>
                ${content.map(item => `<li>${formatItem(item)}</li>`).join("")}
            </ul>
        </div>
    `;
}

