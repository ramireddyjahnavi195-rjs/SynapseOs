function saveToHistory(projectIdea, blueprintTitle) {
    let history = JSON.parse(localStorage.getItem("synapseHistory")) || [];

    const newItem = {
        idea: projectIdea,
        title: blueprintTitle || "Generated Blueprint",
        time: new Date().toLocaleString()
    };

    history.unshift(newItem);
    history = history.slice(0, 5);

    localStorage.setItem("synapseHistory", JSON.stringify(history));
    loadHistory();
}

function loadHistory() {
    const historyList = document.getElementById("historyList");

    if (!historyList) {
        return;
    }

    const history = JSON.parse(localStorage.getItem("synapseHistory")) || [];

    if (history.length === 0) {
        historyList.innerHTML = `
            <p class="no-history">
                No recent blueprints yet. Generate your first blueprint.
            </p>
        `;
        return;
    }

    historyList.innerHTML = history.map((item, index) => `
        <div class="history-item" onclick="reuseHistoryIdea(${index})">
            <h4>${item.title}</h4>
            <p>${item.idea}</p>
            <span>${item.time}</span>
        </div>
    `).join("");
}

function reuseHistoryIdea(index) {
    const history = JSON.parse(localStorage.getItem("synapseHistory")) || [];
    const item = history[index];

    document.getElementById("projectIdea").value = item.idea;

    document.getElementById("projectIdea").scrollIntoView({
        behavior: "smooth",
        block: "center"
    });
}

function clearHistory() {
    localStorage.removeItem("synapseHistory");
    loadHistory();
}
function downloadPDF() {

    const outputBox = document.getElementById("outputBox");

    if (!outputBox || latestBlueprintText === "") {
        alert("No blueprint available to download. Generate one first.");
        return;
    }

    const options = {
        margin: 0.5,
        filename: "SynapseOS_Blueprint_Report.pdf",
        image: {
            type: "jpeg",
            quality: 0.98
        },
        html2canvas: {
            scale: 2
        },
        jsPDF: {
            unit: "in",
            format: "a4",
            orientation: "portrait"
        }
    };

    html2pdf().set(options).from(outputBox).save();
}
function askAssistant() {

    const chatInput = document.getElementById("chatInput");
    const chatBox = document.getElementById("chatBox");

    const question = chatInput.value.trim();

    if (question === "") {
        alert("Please enter a question.");
        return;
    }

    chatBox.innerHTML += `
        <div class="user-message">
            ${question}
        </div>
    `;

    chatInput.value = "";

    chatBox.innerHTML += `
        <div class="assistant-message loading-answer">
            Thinking...
        </div>
    `;

    chatBox.scrollTop = chatBox.scrollHeight;

    fetch("/ask-assistant", {
        method: "POST",
        headers: {
            "Content-Type": "application/json"
        },
        body: JSON.stringify({
            question: question,
            projectContext: latestBlueprintText
        })
    })
    .then(response => response.json())
    .then(data => {

        const loading = document.querySelector(".loading-answer");

        if (loading) {
            loading.remove();
        }

       chatBox.innerHTML += `
    <div class="assistant-message formatted-answer">
        ${formatAIResponse(data.answer)}
    </div>
`;
        chatBox.scrollTop = chatBox.scrollHeight;
    })
    .catch(error => {

        const loading = document.querySelector(".loading-answer");

        if (loading) {
            loading.remove();
        }

        chatBox.innerHTML += `
            <div class="assistant-message">
                Something went wrong while contacting the assistant.
            </div>
        `;

        console.error(error);
    });
}
function formatAIResponse(text) {

    return text

        // Bold headings
        .replace(/\*\*(.*?)\*\*/g, "<strong>$1</strong>")

        // Bullet points
        .replace(/^\s*\*\s(.*)$/gm, "<li>$1</li>")

        // Numbered points
        .replace(/^\s*\d+\.\s(.*)$/gm, "<li>$1</li>")

        // Wrap list items
        .replace(/(<li>.*<\/li>)/gs, "<ul>$1</ul>")

        // Line breaks
        .replace(/\n/g, "<br>");
}